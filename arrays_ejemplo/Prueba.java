
package arrays_ejemplo;

public class Prueba {
    
    private int id;
    private String estatus;
    
    public Prueba (int id, String estatus){
    this.id = id;
    this.estatus = estatus;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEstatus() {
        return estatus;
    }

    public void setEstatus(String estatus) {
        this.estatus = estatus;
    }

    
    
}
